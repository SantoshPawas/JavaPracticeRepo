package Constructor;

public class ConDemo 
{
	int a;
	float b;
	char c;
	boolean d;
	String name;
	public static void main(String[] args)
	{
		ConDemo cd=new ConDemo();
		System.out.println("a="+cd.a);
		System.out.println("b="+cd.b);
		System.out.println("c="+cd.c+"hi");
		System.out.println("d="+cd.d);
		System.out.println("name="+cd.name);
	}

}
