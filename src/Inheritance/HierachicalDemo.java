package Inheritance;
//Demostrate Heirarchical inheritance

public class HierachicalDemo 
{
	public static void main(String[] args)
	{
		Son1 s1=new Son1();
		s1.car();
		s1.money();
		s1.home();
		s1.mobile();
		Son2 s2=new Son2();
		s2.car();
		s2.money();
		s2.home();
		s2.Bike();
		Son3 s3=new Son3();
		s3.Car();
		s3.Money();
		s3.Home();
		s3.Laptop();
		
		
	}

}
