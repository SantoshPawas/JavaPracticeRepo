package Arrays;

public class ArrayDemo1
{
	public static void main(String[] args)
	{
		
		String[] str1=new String[4];		//declaration 
		str1[0]="Rohhit";				//initialisation
		str1[1]="Virat";
		str1[2]="Hardik";
		str1[3]="MSD";
		System.out.println(str1[0]);		//usage
		System.out.println(str1[1]);
		System.out.println(str1[2]);
		System.out.println(str1[3]);
		
		
		
	}

}
